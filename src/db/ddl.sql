CREATE TABLE BOOK(
	ISBN NUMBER(13) PRIMARY KEY,
	GENRE VARCHAR2(30),
	B_NAME VARCHAR2(30),
	WRITER VARCHAR2(30),
	P_RENT INT,
	CLEAR_NUM INT,
	ORIGIN_PRICE INT,
	SUMMARY VARCHAR2(120)
);
 
CREATE TABLE HISTORY(
 	ID VARCHAR2(20) PRIMARY KEY,
 	ISBN NUMBER(13),
 	PRICE INT,
 	CLEAR INT
);
 
CREATE TABLE MEMBER(
 	ID VARCHAR2(20) PRIMARY KEY,
 	PASS VARCHAR2(20),
 	ENAME VARCHAR2(20),
 	EMAIL VARCHAR2(20),
 	GENRE VARCHAR2(20),
 	EGRADE INT,
 	POINT INT,
 	CASH INT
);

 
 
 
 
 
 
 